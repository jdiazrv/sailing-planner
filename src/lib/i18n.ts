export type TranslationKey = keyof (typeof dictionary)["es"];

export const getDictionary = (locale: Locale) => dictionary[locale];

export const t = (locale: Locale, key: TranslationKey) =>
  dictionary[locale][key] ?? dictionary.es[key];

export const getPermissionLabelForLocale = (
  locale: Locale,
  level: PermissionLevel | null | undefined,
  isSuperuser = false,
) => {
  if (isSuperuser && !level) {
    return locale === "es" ? "superusuario" : "superuser";
  }

  if (level === "manager") {
    return locale === "es" ? "gestor" : "manager";
  }

  if (level === "editor") {
    return locale === "es" ? "editor" : "editor";
  }

  return locale === "es" ? "lector" : "viewer";
};

export const getIntlLocale = (locale?: string) =>
  locale === "en" ? "en-US" : "es-ES";

export const getDocumentLocale = (): Locale => {
  if (typeof document === "undefined") {
    return "es";
  }

  return isLocale(document.documentElement.lang)
    ? document.documentElement.lang
    : "es";
};
